def detect_theme(text):
    text_lower = text.lower()
    scores = {}
    for theme, words in KEYWORDS.items():
        count = sum(1 for word in words if word in text_lower)
        if count > 0:
            scores[theme] = count
    return max(scores, key=scores.get) if scores else 'general'

def detect_intent(kws):
    if any(w in kws for w in ['горящий', 'акция', 'скидка', 'спеццена', 'вау', 'бомба']):
        return 'sale'
    if any(w in kws for w in ['vip', 'премиум', 'эксклюзив', 'люкс', '5*']):
        return 'vip'
    if any(w in kws for w in ['семья', 'дети', 'ребёнок', 'семейный']):
        return 'family'
    if any(w in kws for w in ['срочно', 'последний', 'дедлайн', 'таймер']):
        return 'urgent'
    return random.choice(['sale', 'inspirational', 'entertainment', 'storytelling', 'emotional'])

def get_emoji_pool(theme, variant=0):
    base_pool = list(EMOJI_THEMES.get(theme, EMOJI_THEMES['general']))
    general = EMOJI_THEMES['general']
    variants = [
        base_pool + general,
        base_pool[:len(base_pool)//2] + general,
        general + base_pool[len(base_pool)//2:]
    ]
    return list(set(variants[variant % len(variants)]))

def remove_emojis(text):
    if not text:
        return ""
    emoji_pattern = re.compile(
        "[" 
        "\U0001F600-\U0001F64F"
        "\U0001F300-\U0001F5FF"
        "\U0001F680-\U0001F6FF"
        "\U0001F1E0-\U0001F1FF"
        "\U00002702-\U000027B0"
        "]+", 
        flags=re.UNICODE
    )
    cleaned = emoji_pattern.sub('', text)
    cleaned = re.sub(r'\s+', ' ', cleaned).strip()
    return cleaned

def remove_formatting(text):
    if not text:
        return ""
    text = re.sub(r'<b>(.*?)</b>', r'\1', text)
    text = re.sub(r'<i>(.*?)</i>', r'\1', text)
    text = re.sub(r'<u>(.*?)</u>', r'\1', text)
    text = re.sub(r'<a.*?>(.*?)</a>', r'\1', text)
    text = re.sub(r'\*\*(.*?)\*\*', r'\1', text)
    text = re.sub(r'\*(.*?)\*', r'\1', text)
    text = text.replace('**', '').replace('__', '').replace('~~', '')
    return text.strip()

